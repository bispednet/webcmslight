    public static function currentLocale(): string
    {
        $path = parse_url($_SERVER['REQUEST_URI'] ?? '/', PHP_URL_PATH) ?: '/';
        return str_starts_with($path, '/en') ? 'en' : 'it';
    }

    public static function translateHtml(string $html): string
    {
        if (self::currentLocale() !== 'en' || str_starts_with(parse_url($_SERVER['REQUEST_URI'] ?? '/', PHP_URL_PATH) ?: '/', '/admin')) {
            return $html;
        }

        $html = strtr($html, self::dictionary());
        return preg_replace_callback(
            '/\b(href|action)="(\/[^"]*)"/',
            static fn(array $match): string => $match[1] . '="' . self::localizedUrl($match[2], 'en') . '"',
            $html
        ) ?? $html;
    }

    public static function localizedUrl(string $url, string $locale): string
    {
        if ($locale !== 'en' || str_starts_with($url, '/en') || str_starts_with($url, '//') || str_contains($url, 'lang=it')) {
            return $url;
        }
        $map = [
            '/teleassistenza' => '/en/remote-support',
            '/sostenibilita' => '/en/sustainability',
            '/appuntamenti' => '/en/appointments',
            '/area-clienti' => '/en/customer-area',
            '/contatti' => '/en/contact',
            '/azienda' => '/en/company',
            '/servizi' => '/en/services',
            '/products' => '/en/products',
            '/register' => '/en/register',
            '/login' => '/en/login',
            '/legal' => '/en/legal',
            '/dove' => '/en/find-us',
            '/blog' => '/en/blog',
            '/faq' => '/en/faq',
            '/' => '/en/',
        ];
        foreach ($map as $it => $en) {
            if ($url === $it) {
                return $en;
            }
            if ($it !== '/' && str_starts_with($url, $it) && in_array($url[strlen($it)] ?? '', ['/', '?', '#'], true)) {
                return $en . substr($url, strlen($it));
            }
        }
        return $url;
    }

